// Helper function to check if username is disallowed
export function isUsernameDisallowed(username: string) {
  const normalizedUsername = username.toLowerCase().trim();
  return DISALLOWED_USERNAMES.includes(normalizedUsername);
}

// Helper function to check with fuzzy matching (catches variations)
export function isUsernameSimilarToDisallowed(
  username: string,
  threshold = 0.8
) {
  const normalizedUsername = username.toLowerCase().trim();

  // Check exact matches first
  if (DISALLOWED_USERNAMES.includes(normalizedUsername)) {
    return true;
  }

  // Check for common variations
  const variations = [
    normalizedUsername.replace(/[0-9]/g, ""), // Remove numbers
    normalizedUsername.replace(/[_-]/g, ""), // Remove separators
    normalizedUsername.replace(/[aeiou]/g, ""), // Remove vowels
  ];

  return variations.some((variation) =>
    DISALLOWED_USERNAMES.some(
      (disallowed) =>
        variation.includes(disallowed) || disallowed.includes(variation)
    )
  );
}
